uint32_t hdmirx_update_edid(int port_id)
{
	int32_t err, count;
	stm_hdmirx_test_t stm_hdmirx_test;
	stm_hdmirx_port_edid_block_t edid_block;

	HDMI_TEST_PRINT("***********UPDATE EDID TEST************\n");
	err = stm_hdmirx_device_open(&stm_hdmirx_test.dev, 0);
	if (err != 0)
	{
		HDMI_TEST_PRINT("HdmiRx Device Open failed \n");
		return 0;
	}

	err = stm_hdmirx_port_open(stm_hdmirx_test.dev, port_id, &stm_hdmirx_test.port_p[port_id]);
	if (err != 0)
	{
		HDMI_TEST_PRINT("HdmiRx Port Open failed \n");
		stm_hdmirx_device_close(stm_hdmirx_test.dev);
		return 0;
	}
	else
	{
    HDMI_TEST_PRINT("HdmiRx_update Port Open successfully \n");
	}

	stm_hdmirx_port_update_edid_block(stm_hdmirx_test.port_p[port_id], 0, &default_edid1);
	stm_hdmirx_port_update_edid_block(stm_hdmirx_test.port_p[port_id], 1, &default_edid2);

	stm_hdmirx_port_read_edid_block(stm_hdmirx_test.port_p[port_id], 0, &edid_block);
	for (count = 0; count < 128; count++)
	{
		if(edid_block[count] != default_edid1[count])
		{
			HDMI_TEST_PRINT("HdmiRx EDID Check failed in Bolck0[%d] \n", count);
			break;
		}
	}
	stm_hdmirx_port_read_edid_block(stm_hdmirx_test.port_p[port_id], 1, &edid_block);
	for (count = 1; count < 128; count++)
	{
		if(edid_block[count] != default_edid2[count])
		{
			HDMI_TEST_PRINT("HdmiRx EDID Check failed in Bolck1[%d] \n", count);
			break;
		}
	}

	stm_hdmirx_port_close(stm_hdmirx_test.port_p[port_id]);
	stm_hdmirx_device_close(stm_hdmirx_test.dev);
	HDMI_TEST_PRINT("***********UPDATE EDID TEST DONE************\n");

	return 0;
}

STCHIP_Error_t STV0903_Init(Demod_InitParams_t *InitParams,
			    STCHIP_Handle_t *hChipHandle)
{

	int32_t i;

	STCHIP_Handle_t hChip;

	/* fill elements of external chip data structure */
	InitParams->Chip->NbRegs = STV0903_NBREGS;

	InitParams->Chip->NbFields = STV0903_NBFIELDS;

	InitParams->Chip->ChipMode = STCHIP_MODE_SUBADR_16;

	InitParams->Chip->pData = NULL;

#ifdef HOST_PC
	(*hChipHandle) = ChipOpen(InitParams->Chip);

#endif

	hChip = (*hChipHandle);

	if (hChip == NULL)
		return CHIPERR_INVALID_HANDLE;
	hChip->Error = 0;

	/*******************************
	 **   CHIP REGISTER MAP IMAGE INITIALIZATION
	 **     ----------------------
	 ********************************/
	ChipUpdateDefaultValues(hChip, STV0903DefVal);

	/*Read the ID */
	hChip->ChipID = ChipGetOneRegister(hChip, RSTV0903_MID);
	if (hChip->Error != CHIPERR_NO_ERROR)
		return hChip->Error;

	/*******************************
	 **   REGISTER CONFIGURATION
	 **     ----------------------
	 ********************************/
	/*Startup sequence */

	/* Stop the demod  */
	ChipSetOneRegister(hChip, RSTV0903_DMDISTATE, 0x5c);

	/* wait for demod stop (i.e. no more tuner I2C transactions) */
	WAIT_N_MS(3);

	/* put the Tuner HW controller 1 to NO_TUNER mode */
	ChipSetOneRegister(hChip, RSTV0903_TNRCFG, 0x6c);

	/* Configure the I2C repeater to off */
	ChipSetOneRegister(hChip, RSTV0903_I2CRPT, 0x24);

	/* Set the PLL divider */
	ChipSetOneRegister(hChip, RSTV0903_NCOARSE, 0x13);

	WAIT_N_MS(3);	/* WAIT for 3 ms        */

	/* Set the I2C to oversampling ratio 1/41 mode */
	ChipSetOneRegister(hChip, RSTV0903_I2CCFG, 0x08);

	/* enable the PLL and end of STANDBY, clock input = XTALI
	Set this register to 0x20 for clock input = CLKI
	*/
	ChipSetOneRegister(hChip, RSTV0903_SYNTCTRL, 0x22);

	WAIT_N_MS(3);	/* WAIT for 3 ms        */

	/*End of Startup Sequence */

	for (i = 0; i < 102; i++)
		ChipSetOneRegister(hChip, STV0903_InitVal[i][0],
				   STV0903_InitVal[i][1]);

	if (ChipGetOneRegister(hChip, RSTV0903_MID) >= 0x20) {
		/*If cut 2.0 above apply addon init values */
		for (i = 0; i < 23; i++)
			ChipSetOneRegister(hChip, STV0903_Cut20_AddOnVal[i][0],
						STV0903_Cut20_AddOnVal[i][1]);
	}

	ChipSetOneRegister(hChip, RSTV0903_PDELCTRL2, 0x01);

	/*Disable BCH FIFO diagnostics */
	ChipSetOneRegister(hChip, RSTV0903_PDELCTRL3, 0x20);

	ChipSetOneRegister(hChip, RSTV0903_TSTRES0, 0x80);

	ChipSetOneRegister(hChip, RSTV0903_TSTRES0, 0x00);

	/* To select TS output for 900 demod to work using 903 driver */
	ChipSetOneRegister(hChip, RSTV0903_TSGENERAL, 0x08);

	return hChip->Error;
}

STCHIP_Error_t STV0900_Init(Demod_InitParams_t *InitParams,
			    STCHIP_Handle_t *hChipHandle)
{
	S32 i;
	STCHIP_Handle_t hChip;
	STCHIP_Error_t error;

	/* fill elements of external chip data structure */
	InitParams->Chip->NbRegs = STV0900_NBREGS;
	InitParams->Chip->NbFields = STV0900_NBFIELDS;
	InitParams->Chip->ChipMode = STCHIP_MODE_SUBADR_16;
	InitParams->Chip->pData = NULL;

#ifdef HOST_PC
	(*hChipHandle) = ChipOpen(InitParams->Chip);
#endif
	hChip = (*hChipHandle);
	if (hChip != NULL) {

		/*******************************
		 **   CHIP REGISTER MAP IMAGE INITIALIZATION
		 **     ----------------------
		 ********************************/
		ChipUpdateDefaultValues(hChip, STV0900DefVal);

		/*Read the ID */
		hChip->ChipID = ChipGetOneRegister(hChip, RSTV0900_MID);

		if (hChip->Error == CHIPERR_NO_ERROR) {

			/*******************************
			 **   REGISTER CONFIGURATION
			 **     ----------------------
			 ********************************/
			/*Startup sequence */
			/* Stop the demod 1 */
			ChipSetOneRegister(hChip, RSTV0900_P1_DMDISTATE, 0x5c);
			/* Stop the demod 2 */
			ChipSetOneRegister(hChip, RSTV0900_P2_DMDISTATE, 0x5c);
			/* wait for demod stop (i.e. no more tuner I2C
			 * transactions) */
			WAIT_N_MS(3);
			/* put the Tuner HW controller 1 to NO_TUNER mode */
			ChipSetOneRegister(hChip, RSTV0900_P1_TNRCFG, 0x6c);
			/* put the Tuner HW controller 2 to NO_TUNER mode */
			ChipSetOneRegister(hChip, RSTV0900_P2_TNRCFG, 0x6f);
			/* Configure the I2C repeater to off */
			ChipSetOneRegister(hChip, RSTV0900_P1_I2CRPT, 0x24);
			/* Configure the I2C repeater to off */
			ChipSetOneRegister(hChip, RSTV0900_P2_I2CRPT, 0x24);

			/* Set the PLL divider */
			ChipSetOneRegister(hChip, RSTV0900_NCOARSE, 0x13);
			WAIT_N_MS(3);	/* WAIT for 3 ms        */

			/* Set the I2C to oversampling ratio 1/41 mode */
			ChipSetOneRegister(hChip, RSTV0900_I2CCFG, 0x08);
			/* recommendation : The I2C repeater bus should not be
			 * used over 50KHz. */
			/* enable the PLL and end of STANDBY, clock input =
			 * XTALI
			   Set this register to 0x20 for clock input = CLKI */
			ChipSetOneRegister(hChip, RSTV0900_SYNTCTRL, 0x22);
			WAIT_N_MS(3);	/* WAIT for 3 ms        */
			/*End of Startup Sequence */

			/*Continue the registers init sequence */
			for (i = 0; i < 167; i++)
				ChipSetOneRegister(hChip, STV0900_InitVal[i][0],
						   STV0900_InitVal[i][1]);

			if (ChipGetOneRegister(hChip, RSTV0900_MID) >= 0x20) {
				/*
				   by default set TSGENERAL to have path1
				   serial on TS1, path2 serial on TS2 then to
				   change this setting use the function
				   "SetTS_Parallel_Serial"
				 */
				ChipSetOneRegister(hChip, RSTV0900_TSGENERAL,
						   0x0c);
				/*If cut 2.0 above apply addon init values */
				for (i = 0; i < 32; i++)
					ChipSetOneRegister(hChip,
						STV0900_Cut20_AddOnVal[i][0],
						STV0900_Cut20_AddOnVal[i][1]);
			}

			/*Set FECSPY for PER measure */
			ChipSetOneRegister(hChip, RSTV0900_P1_FSPYCFG, 0x6C);
			/*Set FECSPY for PER measure */
			ChipSetOneRegister(hChip, RSTV0900_P2_FSPYCFG, 0x6C);

			/*Disable the BCH fifo for cut 3.0, only for lab
			 * measurment using the FECspy */
			ChipSetOneRegister(hChip, RSTV0900_P1_PDELCTRL2, 0x01);
			ChipSetOneRegister(hChip, RSTV0900_P2_PDELCTRL2, 0x21);

			ChipSetOneRegister(hChip, RSTV0900_P1_PDELCTRL3, 0x20);
			ChipSetOneRegister(hChip, RSTV0900_P2_PDELCTRL3, 0x20);

			/*LDPC reset */
			ChipSetOneRegister(hChip, RSTV0900_TSTRES0, 0x80);
			ChipSetOneRegister(hChip, RSTV0900_TSTRES0, 0x00);

		}

		error = hChip->Error;
	} else
		error = CHIPERR_INVALID_HANDLE;

	return error;
}
